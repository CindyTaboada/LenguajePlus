package model;

public class ClaseNivel {
    private int idClase;
    private int idNivel;
    
	public ClaseNivel(int idClase, int idNivel) {
	
		this.idClase = idClase;
		this.idNivel = idNivel;
	}

	public int getIdClase() {
		return idClase;
	}

	public int getIdNivel() {
		return idNivel;
	}
    
    

}
