package model;

public class Lenjuages {
	private int idLenguaje;
	private String tipoLenguaje;

	public Lenjuages(int idLenguaje, String tipoLenguaje) {
		super();
		this.idLenguaje = idLenguaje;
		this.tipoLenguaje = tipoLenguaje;
	}

	public int getIdLenguaje() {
		return idLenguaje;
	}

	public String getTipoLenguaje() {
		return tipoLenguaje;
	}

}
